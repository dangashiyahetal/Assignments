function numbers(i) {
  if (i == 1) {
    alert("ONE");
  } else if (i == 2) {
    alert("two");
  } else if (i == 3) {
    alert("three");
  } else if (i == 4) {
    alert("four");
  } else if (i == 5) {
    alert("five");
  } else {
    alert("Please enter a Valid Number");
  }
}
numbers(prompt("Enter a digit: "));
