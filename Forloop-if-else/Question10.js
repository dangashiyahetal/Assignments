function Multiplecrit(numbers) {
  if (numbers >= 1 && numbers <= 10) {
    alert("1 and 10");
  } else if (numbers >= 11 && numbers <= 20) {
    alert("11 and 20");
  } else if (numbers >= 21 && numbers <= 30) {
    alert("21 and 30");
  } else if (numbers >= 31 && numbers <= 40) {
    alert("31 and 40");
  } else if (numbers >= 41 && numbers <= 50) {
    alert("41 and 50");
  }
}
Multiplecrit(prompt("Enter number of your choice:"));
