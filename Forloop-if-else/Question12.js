function ename(params) {
  if (params == "one") {
    alert("1");
  } else if (params == "TWO") {
    alert("2");
  } else if (params == "THREE") {
    alert("3");
  } else if (params == "FOUR") {
    alert("4");
  } else if (params == "FIVE") {
    alert("5");
  }
}
ename(prompt("Enter a digit: "));
