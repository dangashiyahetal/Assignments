function numbers(params) {
  if (params == 123) {
    alert("One Two Three");
  } else if (params == 124) {
    alert("One Two Four");
  } else if (params == 125) {
    alert("One Two Five");
  } else if (params == 126) {
    alert("One Two Six");
  } else if (params == 127) {
    alert("One Two Seven");
  }
}
numbers(prompt("Enter the number from 123-127: "));
