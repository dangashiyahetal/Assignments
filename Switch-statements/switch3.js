function switchcase(year) {
  switch (parseInt(year)) {
    case 2014:
      alert("12 Years a Slave");
      break;
    case 2015:
      alert("	Birdman");
      break;
    case 2016:
      alert("Spotlight");
      break;
    case 2017:
      alert("Moonlight");
      break;
    case 2018:
      alert("	The Shape of Water");
      break;
    case 2019:
      alert("Parasite");
      break;
    case 2020:
      alert("Joker");
      break;
    case 2021:
      alert("The Guardian");
  }
}
switchcase(prompt("Enter Year"));
