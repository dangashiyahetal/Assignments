function movieswonoscars(year) {
  if (year == 2014) {
    alert("12 Years a Slave");
  } else if (year == 2015) {
    alert("	Birdman");
  } else if (year == 2016) {
    alert("Spotlight");
  } else if (year == 2017) {
    alert("Moonlight");
  } else if (year == 2018) {
    alert("	The Shape of Water");
  } else if (year == 2019) {
    alert("Parasite");
  } else if (year == 2020) {
    alert("Joker");
  } else if (year == 2021) {
    alert("The Guardian");
  }
}

movieswonoscars(prompt("Please enter the year"));
