function Entergrade(Grade) {
  if (Grade == "A+") {
    alert("Excellent");
  }
  if (Grade == "A") {
    alert("Good Job");
  }
  if (Grade == "A-") {
    alert("Average");
  }
  if (Grade == "B+") {
    alert("welldone");
  }
  if (Grade == "B-") {
    alert("Slightly Poor");
  }
  if (Grade == "C-") {
    alert("very Poor");
  }
}

Entergrade(prompt("Enter the Grade"));
